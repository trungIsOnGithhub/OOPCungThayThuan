	private void nutThemVaoActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_nutThemVaoActionPerformed

		this.mainTableModel = (DefaultTableModel) table.getModel();
		this.mainTableModel.setRowCount(0);

		ArrayList<MenuItemDisplayer> menuItemDisplayer = mpd.getMainMenuDisplayerList();

		Integer totalPrice = 0;

		for (var item : menuItemDisplayer) {
			int valueFromSpinner = item.getValueFromSpinner();

			if (valueFromSpinner > 0) {
				String[] nameAndPrice = item.getText().split(" ");

				this.mainTableModel
						.addRow(new Object[] { nameAndPrice[0], nameAndPrice[1], Integer.toString(valueFromSpinner) });

				totalPrice += valueFromSpinner * Integer.valueOf(nameAndPrice[1]);
			}

		}

		this.totalAmountDisplayer.setText(Integer.toString(totalPrice));
	}// GEN-LAST:event_nutThemVaoActionPerformed

