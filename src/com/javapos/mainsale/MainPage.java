	private void chotDonButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_chotDonButtonActionPerformed
		// TODO add your handling code here:

		CustomerOrder tempOrder = new CustomerOrder();

		String phoneNumberFromMenu = "unknown";
		try {
			phoneNumberFromMenu = this.soDienThoai.getText();
		} catch (NullPointerException except) {
			phoneNumberFromMenu = "unknown";
		} finally {
			if (phoneNumberFromMenu.equals("")) {
				phoneNumberFromMenu = "unknown";
			}
		}

		String totalPriceFromMenu = "0";
		try {
			totalPriceFromMenu = this.totalAmountDisplayer.getText();
		} catch (NullPointerException excep) {
			// Ta loai bo cac don hang rong va khong ghi chung vao danh sach
//            System.out.println("Order khong ton tai mat hang nao.");
			return;
		} finally {
			// Ta loai bo cac don hang rong va khong ghi chung vao danh sach
			if (totalPriceFromMenu.equals("") || totalPriceFromMenu.equals("0") || totalPriceFromMenu.equals("-1")) {
//                System.out.println("Order khong ton tai mat hang nao.");
				return;
			}
		}

		tempOrder.setUpOrderInfo(phoneNumberFromMenu, totalPriceFromMenu);

		this.orderViewerPage.currentSessionHistory.addNewOrderToHistory(tempOrder);

		// Reset cac Component ve trang thai cu sau khi an nut chot don.
		DefaultTableModel dfm = (DefaultTableModel) this.table.getModel();
		dfm.setRowCount(0);

		this.soDienThoai.setText("");
		this.totalAmountDisplayer.setText("");
		this.customerPayAmount.setText("");
		this.needToReturnAmount.setText("");

		// Nut nay chi an duoc khi don hang dang o trang thai co the thanh toan
		this.chotDonButton.setEnabled(false);
	}// GEN-LAST:event_chotDonButtonActionPerformed

