	private void customerPaymentOKButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_customerPaymentOKButtonActionPerformed

		// Input nhap vao phan "So tien khach hang dua" co the khong hop le
		String customerPaymentAmount = this.customerPayAmount.getText();
		String totalCost = this.totalAmountDisplayer.getText();

		if (customerPaymentAmount != null && !customerPaymentAmount.equals("") && totalCost != null
				&& !totalCost.equals("") && !totalCost.equals("0")) {
			// Neu truong hop don hang khong co hoa don nao thi khong tinh

			try {
				int pay = Integer.parseInt(customerPaymentAmount);
				int cost = Integer.parseInt(totalCost);

				int returnAmount = pay - cost;

				if (returnAmount < 0) {
					this.needToReturnAmount.setText("Khong du tien!");
				} else {
					this.needToReturnAmount.setText(Integer.toString(returnAmount));
					this.chotDonButton.setEnabled(true);
				}
			} catch (NumberFormatException except) {
				this.needToReturnAmount.setText("Khong hop le!");

			}
			// Neu doan code throw Exception, "So tien khach hang dua" da bi nhap sai.
		}

	}// GEN-LAST:event_customerPaymentOKButtonActionPerformed

